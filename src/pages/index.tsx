import { type NextPage } from "next";
import Head from "next/head";
import { useState } from "react";
import { api } from "../utils/api";

import MovieCard from "../components/MovieCard";

const Home: NextPage = () => {
  const { data: moviesData } = api.movies.getAllMovies.useQuery();

  const categories = [
    "Currently Showing",
    "Coming Soon",
    "Drama",
    "Comedy",
    "Action",
    "Fantasy",
    "Horror",
    "Romance",
    "Western",
  ];

  const trailerIds = [
    "7L8p7_SLzvU",
    "DuWEEKeJLMI",
    "JNwNXF9Y6kY",
    "lc0UehYemQA",
    "vZ734NWnAHA",
  ];

  const movies = moviesData?.map((movie, index) => ({
    ...movie,
    trailerVideoId: trailerIds[index % trailerIds.length],
  }));

  return (
    <>
      <Head>
        <title>Cinema EBooking System</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="bg-white">
        <div className="mx-64 mt-8 grid grid-cols-12 gap-8 ">
          <input
            className="col-span-3 rounded border border-black"
            placeholder="Search Movies"
          />
          {categories.map((category) => {
            return (
              <button
                className={`rounded-full  bg-cyan-300 hover:bg-cyan-500`}
                key={category}
              >
                {category}
              </button>
            );
          })}
        </div>
        <div className="mx-64 mt-10 font-bold">Now Showing</div>
        <div className="mx-64 mt-8 grid grid-cols-3 gap-8">
          {movies?.map((movie) => {
            return <MovieCard movie={movie} key={`MovieCard-${movie.id}`} />;
          })}
        </div>
        <div className="mx-64 mt-10 font-bold">Coming Soon</div>
        <div className="mx-64 mt-8 grid grid-cols-3 gap-8">
          {movies?.map((movie) => {
            return <MovieCard movie={movie} key={`MovieCard-${movie.id}`} />;
          })}
        </div>
      </main>
    </>
  );
};

export default Home;
